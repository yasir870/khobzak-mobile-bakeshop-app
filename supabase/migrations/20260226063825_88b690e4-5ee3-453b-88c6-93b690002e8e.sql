  );

-- Customers can view delivery codes for their orders
CREATE POLICY "Customers can view own delivery codes"
  ON public.delivery_codes FOR SELECT
  USING (
    auth.uid() IS NOT NULL AND
    EXISTS (
      SELECT 1 FROM profiles WHERE profiles.id = auth.uid() AND profiles.phone = delivery_codes.customer_phone
