      SELECT 1 FROM profiles p JOIN drivers d ON p.phone = d.phone
      WHERE p.id = auth.uid() AND p.user_type = 'driver' AND d.id = delivery_codes.driver_id
    )
  );

-- Drivers can update delivery codes (mark as used)
CREATE POLICY "Drivers can update own delivery codes"
  ON public.delivery_codes FOR UPDATE
  USING (
    auth.uid() IS NOT NULL AND
    EXISTS (
      SELECT 1 FROM profiles p JOIN drivers d ON p.phone = d.phone
      WHERE p.id = auth.uid() AND p.user_type = 'driver' AND d.id = delivery_codes.driver_id
